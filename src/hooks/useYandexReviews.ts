export const useYandexReviews = () => {
  const [testimonials, setTestimonials] = useState<Testimonial[]>(defaultTestimonials);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    const getReviews = async () => {
      try {
        setLoading(true);
        const yandexReviews = await fetchYandexReviews();
        
        if (yandexReviews.length > 0) {
          // Format reviews as testimonials
          const formattedReviews = formatYandexReviewsAsTestimonials(yandexReviews);
          setTestimonials(formattedReviews);
        }
        
        setError(null);
      } catch (err) {
        console.error('Error fetching Yandex reviews:', err);
        setError('Failed to fetch reviews. Using fallback data.');
        // Keep using the default testimonials
      } finally {
        setLoading(false);
      }
    };

    getReviews();
  }, []);

  return { testimonials, loading, error };
};

export default useYandexReviews; 
